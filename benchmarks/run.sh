#!/usr/bin/env bash
#
# Compares Aurora against make, just and task on the same task graph.
#
# Usage: benchmarks/run.sh [--runs N]
#
# Needs: hyperfine, gmake (GNU make >= 4), just, task, python3.
# Results go to benchmarks/results.md. They are machine-specific: rerun locally
# rather than trusting a number measured on someone else's laptop.

set -euo pipefail

runs=10
while [ $# -gt 0 ]; do
  case "$1" in
    --runs) runs="$2"; shift 2 ;;
    *) echo "unknown argument: $1" >&2; exit 2 ;;
  esac
done

root="$(cd "$(dirname "$0")/.." && pwd)"
bench="$root/benchmarks"
work="$(mktemp -d)"
trap 'rm -rf "$work"' EXIT

# Apple ships GNU make 3.81 (2006). Benchmarking against it would be a rigged
# fight, so prefer a modern one when it is installed.
if command -v gmake >/dev/null 2>&1; then
  MAKE=gmake
elif [ -x /opt/homebrew/opt/make/libexec/gnubin/make ]; then
  MAKE=/opt/homebrew/opt/make/libexec/gnubin/make
else
  MAKE="make"
fi

AURORA="${AURORA:-$root/target/release/aurora}"
[ -x "$AURORA" ] || { echo "aurora binary not found at $AURORA (cargo build --release)" >&2; exit 1; }

for tool in hyperfine just task python3; do
  command -v "$tool" >/dev/null 2>&1 || { echo "missing: $tool" >&2; exit 1; }
done

jobs="$(getconf _NPROCESSORS_ONLN 2>/dev/null || echo 8)"
out="$bench/results.md"

{
  echo "# Benchmark results"
  echo
  echo "Generated by \`benchmarks/run.sh\`. Numbers are specific to the machine below."
  echo
  echo '```'
  echo "date    : $(date -u '+%Y-%m-%d %H:%M UTC')"
  echo "os      : $(uname -sr)"
  echo "cpu     : $(sysctl -n machdep.cpu.brand_string 2>/dev/null || grep -m1 'model name' /proc/cpuinfo | cut -d: -f2- | xargs)"
  echo "cores   : $jobs"
  echo "aurora  : $("$AURORA" --version)"
  echo "make    : $($MAKE --version | head -1)"
  echo "just    : $(just --version)"
  echo "task    : $(task --version)"
  echo '```'
} > "$out"

scenario() {
  local title="$1" shape="$2" tasks="$3" body="$4" note="$5"
  echo "  -> $title"
  python3 "$bench/generate.py" --shape "$shape" --tasks "$tasks" --body "$body" --out "$work" >/dev/null

  {
    echo
    echo "## $title"
    echo
    echo "$note"
    echo
  } >> "$out"

  ( cd "$work" && hyperfine \
      --warmup 3 --runs "$runs" \
      --export-markdown "$work/table.md" \
      --command-name "aurora"          "$AURORA all --no-tui --no-cache" \
      --command-name "make -j$jobs"    "$MAKE -j$jobs all" \
      --command-name "make (no -j)"    "$MAKE all" \
      --command-name "just"            "just all" \
      --command-name "task"            "task all" \
      >/dev/null 2>&1 )

  cat "$work/table.md" >> "$out"
}

echo "Running benchmarks ($runs runs each)..."

scenario "Parallel speed-up" fan-out 8 "sleep 0.5" \
  "8 independent tasks of 0.5s. A runner that parallelises finishes in ~0.5s, one that does not in ~4s."

scenario "Runner overhead" fan-out 100 "true" \
  "100 independent tasks that do nothing. Nothing is measured here but the runner itself: startup, parsing, graph construction and scheduling."

scenario "Scheduling a chain" chain 50 "true" \
  "A 50-task linear chain. No runner can parallelise it, so this isolates per-task scheduling cost from any parallelism gain."

echo
echo "Wrote $out"
